#include <iostream>
using namespace std;
int main() {
  int height;
  cout << "請輸入幾層(1~8):";
  cin >> height;
  if(height > 8 || height < 1)
    cout << "超出範圍";
  else
  for(int a=1, K=7; a<height+1; a++,K--){
    for (int j=0; j<K; j++) {
      cout<<" ";
      }
    for(int i=0; i<a; i++){
      cout<<"#";
      }
    cout<<" ";
    for(int c=0; c<a; c++){
      cout<<"#";
      }
    cout<<"\n";
    }
    }
